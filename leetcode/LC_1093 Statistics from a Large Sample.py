class Solution(object):
    def _getValueByAccuCount(self, count, accCount):
        curAccuCount = 0
        for i in range(len(count)):
            val = count[i]
            curAccuCount += val
            if curAccuCount >= accCount:
                return i

    def sampleStats(self, count):
        minVal, maxVal = float('inf'), -float('inf')
        countPair, totSum, totCount = (0, None), 0, 0
        for i in range(len(count)):
            val = count[i]
            if val > 0:
                minVal = min(minVal, i)
                maxVal = max(maxVal, i)
                if val > countPair[0]:
                    countPair = (val, i)
                totSum += val * i
                totCount += val
        # print("totCount: ", totCount)
        if totCount % 2 == 0:
           median = 1.*(self._getValueByAccuCount(count, totCount/2) + self._getValueByAccuCount(count, (totCount/2+1))) / 2
        else:
            median = 1.*self._getValueByAccuCount(count, totCount/2+1)
        return (1.*minVal, 1.*maxVal, 1.*totSum/totCount, median, 1.*countPair[1])

